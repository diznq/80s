const unsigned char round_constant[10] = {0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36};

void aes_128_enc(const char *data, const char *key, char *out)
{
    unsigned char local_key[16], *uout = (unsigned char*)out;
    unsigned char buf1, buf2, buf3, buf4, round, i;
    memcpy(uout, data, 16);
    memcpy(local_key, key, 16);

    for (round = 0; round < 10; round++)
    {
        for (i = 0; i < 16; i++)
        {
            uout[i] = sbox[uout[i] ^ local_key[i]];
        }

        buf1 = uout[1];
        uout[1] = uout[5];
        uout[5] = uout[9];
        uout[9] = uout[13];
        uout[13] = buf1;

        buf1 = uout[2];
        buf2 = uout[6];
        uout[2] = uout[10];
        uout[6] = uout[14];
        uout[10] = buf1;
        uout[14] = buf2;

        buf1 = uout[15];
        uout[15] = uout[11];
        uout[11] = uout[7];
        uout[7] = uout[3];
        uout[3] = buf1;

        if (round < 9)
        {
            for (i = 0; i < 4; i++)
            {
                buf4 = (i << 2);
                buf1 = uout[buf4] ^ uout[buf4 + 1] ^ uout[buf4 + 2] ^ uout[buf4 + 3];
                buf2 = uout[buf4];
                buf3 = uout[buf4] ^ uout[buf4 + 1];
                buf3 = galois_mul2[buf3];
                uout[buf4] = uout[buf4] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 1] ^ uout[buf4 + 2];
                buf3 = galois_mul2[buf3];
                uout[buf4 + 1] = uout[buf4 + 1] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 2] ^ uout[buf4 + 3];
                buf3 = galois_mul2[buf3];
                uout[buf4 + 2] = uout[buf4 + 2] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 3] ^ buf2;
                buf3 = galois_mul2[buf3];
                uout[buf4 + 3] = uout[buf4 + 3] ^ buf3 ^ buf1;
            }
        }

        local_key[0] = sbox[local_key[13]] ^ local_key[0] ^ round_constant[round];
        local_key[1] = sbox[local_key[14]] ^ local_key[1];
        local_key[2] = sbox[local_key[15]] ^ local_key[2];
        local_key[3] = sbox[local_key[12]] ^ local_key[3];
        for (i = 4; i < 16; i++)
        {
            local_key[i] = local_key[i] ^ local_key[i - 4];
        }
    }

    for (i = 0; i < 16; i++)
    {
        uout[i] = uout[i] ^ local_key[i];
    }
}

void aes_128_dec(const char *data, const char *key, char *out)
{
    unsigned char local_key[16], *uout = (unsigned char*)out;
    unsigned char buf1, buf2, buf3, buf4, round, i;
    memcpy(uout, data, 16);
    memcpy(local_key, key, 16);

    for (round = 0; round < 10; round++)
    {
        local_key[0] = sbox[local_key[13]] ^ local_key[0] ^ round_constant[round];
        local_key[1] = sbox[local_key[14]] ^ local_key[1];
        local_key[2] = sbox[local_key[15]] ^ local_key[2];
        local_key[3] = sbox[local_key[12]] ^ local_key[3];
        for (i = 4; i < 16; i++)
        {
            local_key[i] = local_key[i] ^ local_key[i - 4];
        }
    }
    
    for (i = 0; i < 16; i++)
    {
        uout[i] = uout[i] ^ local_key[i];
    }
    
    for (round = 0; round < 10; round++)
    {
        for (i = 15; i > 3; --i)
        {
            local_key[i] = local_key[i] ^ local_key[i - 4];
        }
        local_key[0] = sbox[local_key[13]] ^ local_key[0] ^ round_constant[9 - round];
        local_key[1] = sbox[local_key[14]] ^ local_key[1];
        local_key[2] = sbox[local_key[15]] ^ local_key[2];
        local_key[3] = sbox[local_key[12]] ^ local_key[3];
        
        if (round > 0)
        {
            for (i = 0; i < 4; i++)
            {
                buf4 = (i << 2);
                
                buf1 = galois_mul2[galois_mul2[uout[buf4] ^ uout[buf4 + 2]]];
                buf2 = galois_mul2[galois_mul2[uout[buf4 + 1] ^ uout[buf4 + 3]]];
                uout[buf4] ^= buf1;
                uout[buf4 + 1] ^= buf2;
                uout[buf4 + 2] ^= buf1;
                uout[buf4 + 3] ^= buf2;
                
                buf1 = uout[buf4] ^ uout[buf4 + 1] ^ uout[buf4 + 2] ^ uout[buf4 + 3];
                buf2 = uout[buf4];
                buf3 = uout[buf4] ^ uout[buf4 + 1];
                buf3 = galois_mul2[buf3];
                uout[buf4] = uout[buf4] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 1] ^ uout[buf4 + 2];
                buf3 = galois_mul2[buf3];
                uout[buf4 + 1] = uout[buf4 + 1] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 2] ^ uout[buf4 + 3];
                buf3 = galois_mul2[buf3];
                uout[buf4 + 2] = uout[buf4 + 2] ^ buf3 ^ buf1;
                buf3 = uout[buf4 + 3] ^ buf2;
                buf3 = galois_mul2[buf3];
                uout[buf4 + 3] = uout[buf4 + 3] ^ buf3 ^ buf1;
            }
        }

        buf1 = uout[13];
        uout[13] = uout[9];
        uout[9] = uout[5];
        uout[5] = uout[1];
        uout[1] = buf1;
        
        buf1 = uout[10];
        buf2 = uout[14];
        uout[10] = uout[2];
        uout[14] = uout[6];
        uout[2] = buf1;
        uout[6] = buf2;
        
        buf1 = uout[3];
        uout[3] = uout[7];
        uout[7] = uout[11];
        uout[11] = uout[15];
        uout[15] = buf1;

        for (i = 0; i < 16; i++)
        {
            uout[i] = reverse_sbox[uout[i]] ^ local_key[i];
        }
    }
}

void aes_128_cbc_enc(const char *data, size_t len, const char *key, const char *iv, char *out) {
    size_t i = 0, j = 0;
    char buf[16];
    memcpy(buf, iv, 16);
    for(i = 0; i < len; i += 16) {
        for(j = 0; j < 16; j++) {
            buf[j] = buf[j] ^ data[i + j];
        }
        aes_128_enc(buf, key, out + i);
    }
}

void aes_128_cbc_dec(const char *data, size_t len, const char *key, const char *iv, char *out) {
    size_t i = 0, j = 0;
    char buf[16], t;
    memcpy(buf, iv, 16);
    for(i = 0; i < len; i += 16) {
        aes_128_dec(data + i, key, out + i);
        for(j = 0; j < 16; j++) {
            t = out[j + i];
            out[j + i] = out[j + i] ^ buf[j];
            buf[j] = t;
        }
    }
}